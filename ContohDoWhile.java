public class ContohDoWhile {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("Perulangan ke- " + i);
            i++;
        }
        int p = 1;
        do {
            System.out.println("Perulangan ke- " + p);
            p++;
        } while (p <= 10);
    }
}
